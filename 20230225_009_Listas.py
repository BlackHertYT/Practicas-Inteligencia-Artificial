# Se define una lista llamada "colores" que contiene varios elementos de tipo string.
colores = ["rojo", "azul", "verde", "amarillo", "rosa"]

# Se utiliza la sintaxis de indexación para acceder al elemento en la posición 3 de la lista "colores".
# El elemento es "amarillo" porque las listas en Python empiezan a contar desde 0.
print(colores[3])

# Se utiliza el método "index()" para buscar la posición del elemento "azul" en la lista "colores".
# El resultado es 1, que es la posición del elemento en la lista.
print(colores.index("azul"))

# Se utiliza el método "index()" para buscar la posición del elemento "rojo" en la lista "colores".
# El resultado es 0, que es la posición del elemento en la lista.
print(colores.index("rojo"))

