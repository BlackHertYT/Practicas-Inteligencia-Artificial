# Se define una variable llamada "Redondear" y se le asigna el valor de un número con decimales.
Redondear = 3.14163224566432134

# Se utiliza la función "round()" para redondear el número a 5 decimales.
# El resultado del redondeo se almacena nuevamente en la variable "Redondear".
Redondear = round(Redondear, 5)

# Se utiliza la función "print()" para imprimir el resultado del redondeo en la consola.
print(Redondear)
