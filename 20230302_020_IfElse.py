#El programa define una variable llamada "numero" y le asigna el valor de 13. 
numero = 13

#Luego, verifica si el valor de "numero" es igual a 13 usando una estructura de control "if". Si el valor de "numero" es igual a 13, el programa imprime "El numero es 13." en la consola. De lo contrario, el programa imprime "El numero es otro numero".
if numero == 13:
    print("El numero es 13.")
else:
    print("El numero es otro numero")